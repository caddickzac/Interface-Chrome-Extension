$(function() {
  $("#ld_a_label_input").on("keyup", function(e) {
    const regex = /^[a-zA-Z]+$/;
    if (!regex.test(this.value)) {
      this.value = this.value.replace(/[`]/g, ''); // note: added 'g' to remove all backticks
    }
  });
});
